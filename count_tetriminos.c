#include "fillit.h"

int		count_tetriminos(char *s)
{
	int	count;
	int	i;

	i = 0;
	count = 0;
	while (s[i] != '\0')
	{
		if (s[i] == '\n' && (s[i + 1] == '\n' || s[i + 1] == '\0'))
			count++;
		i++;
	}
	return (count);
}
